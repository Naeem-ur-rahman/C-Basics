#include <iostream>
#include <math.h>
using namespace std;
int main()
{
    int a =-29;
	int b = 5 ;
	int d = 0 ;
    float c=7.52;
    
    cout << "Square root of "<< b <<"  : "<< sqrt(b)<< endl;
    
    cout << "Absolute value of "<< a << "  : "<< abs(a)<< endl;
    
    cout << "Power (2) of "<< c << "   : "<< pow(c,2)<< endl;
    
    cout << "Power (4) of "<< b << "   : "<< pow(b,4)<< endl;
    
    cout << "Sin value of "<< c << "   : "<< sin(c)<< endl;
    
    cout << "Cos value of "<< d << "   : "<< cos(d)<< endl;

    cout << "Tangent value of "<< b << "  :"<< tan(b)<< endl;
    
    return 0;
}
